#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	char	*str;
	char	*src;

	str = s1;
	src = s2;
	while (*str == *src && *str)
	{
		str++;
		src++;
	}
	return (*str - *src);
}
